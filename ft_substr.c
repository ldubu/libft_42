#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*new_s;
	size_t	i;

	new_s = malloc(sizeof(char) * len);
	if (new_s == NULL)
		return (NULL);
	i = 0;
	while (len-- > 0)
		new_s[i++] = s[start++];
	return (new_s);
}
