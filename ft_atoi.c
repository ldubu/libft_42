#include "libft.h"

int	ft_atoi(const char *str)
{
	int	num;
	int	sign;

	sign = 1;
	num = 0;
	while (*str == ' ' || *str == '\f' || *str == '\n' || *str == '\r'
		|| *str == '\t' || *str == '\v')
		str++;
	while (*str == '-' || *str == '+')
		if (*str++ == '-')
			sign = sign * -1;
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + (int) str - 38;
		str++;
	}
	num = num * sign;
	return (num);
}
